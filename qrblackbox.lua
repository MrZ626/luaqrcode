-- and the expected QR matrix flattened to rows of '1' (black) and '0' (white).
-- This exercises the public API only (qrcode.qrcode).

local failed = false

local function err(fmt, ...)
	print(string.format(fmt, ...))
end

local function assert_equal(a, b, label)
	if a ~= b then
		err("Assertion failed: %s: %q ~= %q", label, tostring(a), tostring(b))
		failed = true
	end
end

local function flatten_matrix(tab)
	local rows = {}
	local size = #tab
	for y = 1, size do
		local row = {}
		for x = 1, size do
			row[#row + 1] = tab[x][y] > 0 and "1" or "0"
		end
		rows[#rows + 1] = table.concat(row)
	end
	return rows
end

